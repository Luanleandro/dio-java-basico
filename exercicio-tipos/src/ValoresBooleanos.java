public class ValoresBooleanos {
    public static void main(String[] args) {
        boolean verdadeiro = true;
        boolean falso = false;

        // Operações lógicas
        System.out.println("Operação lógica E: " + (verdadeiro && falso));
        System.out.println("Operação lógica OU: " + (verdadeiro || falso));
        System.out.println("Operação lógica NÃO: " + (!verdadeiro));
    }
}